// –í—Ä–µ–º—è: O(n)
// –ü–∞–º—è—Ç—å: O(1) (–∏—Å–ø–æ–ª—å–∑—É–µ–º –≤—Å–µ–≥–æ 2 –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ)

// 9 
// -2 1 -3 4 -1 2 1 -5 4
// 6

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // –í–≤–æ–¥ –¥–ª–∏–Ω—ã –º–∞—Å—Å–∏–≤–∞
        int n = sc.nextInt();
        int[] arra = new int[n];

        // –í–≤–æ–¥ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Å—Å–∏–≤–∞
        for (int i = 0; i < n; i++) {
            arra[i] = sc.nextInt();
        }

        // –ê–ª–≥–æ—Ä–∏—Ç–º –ö–∞–¥–∞–Ω–µ
        int maxSum = arra[0];
        int currentSum = arra[0];

        for (int i = 1; i < n; i++) {
            currentSum = Math.max(arra[i], currentSum + arra[i]);
            maxSum = Math.max(maxSum, currentSum);
        }

        System.out.println(maxSum);
    }
}
